const I18n = {
  lang: "es",

  init() {
    const saved = localStorage.getItem("ep-lang");
    const browser = (navigator.language || "es").startsWith("en") ? "en" : "es";
    this.lang = saved || browser;
    this.apply();
  },

  t(key) {
    return I18N[this.lang][key] ?? I18N.es[key] ?? key;
  },

  getLang() {
    return this.lang;
  },

  setLang(lang) {
    this.lang = lang;
    localStorage.setItem("ep-lang", lang);
    document.documentElement.lang = lang;
    this.apply();
    if (typeof window.onLanguageChange === "function") window.onLanguageChange();
  },

  apply() {
    document.querySelectorAll("[data-i18n]").forEach((el) => {
      const key = el.getAttribute("data-i18n");
      const val = this.t(key);
      if (el.tagName === "INPUT" || el.tagName === "TEXTAREA") {
        if (el.hasAttribute("data-i18n-placeholder")) el.placeholder = val;
      } else {
        el.textContent = val;
      }
    });
    document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
      el.placeholder = this.t(el.getAttribute("data-i18n-placeholder"));
    });
    document.querySelectorAll("[data-i18n-aria]").forEach((el) => {
      el.setAttribute("aria-label", this.t(el.getAttribute("data-i18n-aria")));
    });
    document.title = this.t("pageTitle");
    const meta = document.querySelector('meta[name="description"]');
    if (meta) meta.setAttribute("content", this.t("metaDescription"));

    document.querySelectorAll(".lang-btn").forEach((btn) => {
      const active = btn.dataset.lang === this.lang;
      btn.classList.toggle("active", active);
      btn.setAttribute("aria-pressed", String(active));
    });
  },

  field(obj, field) {
    if (!obj) return "";
    if (typeof obj === "string") return obj;
    return obj[this.lang] || obj.es || obj.en || "";
  }
};
